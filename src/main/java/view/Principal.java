private void atualizaTabela(){
    DefaultTableModel modelo = (DefaultTableModel) tblLivro.getModel();
    modelo.setNumRows(0);

    try {
        LivrosDAO livrosDAO = new LivrosDAO();
        ArrayList<Livros> livros = livrosDAO.retornarListaDeLivros(idUsuario);

        for(Livros l : livros){
            modelo.addRow(new Object[]{
                l.getNome(),
                l.getEditora(),
                l.getAno(),
                l.isDisponivel()
            });
        }

    } catch (SQLException e) {
        System.out.println("ERRO, ao carregar tabela na view -> " + e);
    }
}

