    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            Livros l = new Livros();   
            l.setNome(txtTitulo.getText());
            l.setEditora(txtEditora.getText());
            l.setAno(Integer.parseInt(txtAno.getText()));
            if (rdbSim.isSelected()) {
                l.setDisponivel(true);
            } else if (rdbNao.isSelected()) {
                l.setDisponivel(false);
            } else {
                System.out.println("Selecione se o livro esta disponivel.");
                return;
            }
            l.setUsuario_id(this.idUsuario);

            livrosDAO.salvar(l); // 

            atualizaTabela();

            txtTitulo.setText("");        
            txtEditora.setText("");
            txtAno.setText("");
            rdbSim.setSelected(false);
            rdbNao.setSelected(false);

            System.out.println("Livro salvo com sucesso!");
        } catch (SQLException e) {
            System.out.println("Erro ao salvar Livro: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

